<h1>Framework</h1>
<p>> Pengertian Framework :</p>
<p>Framework adalah kerangka kerja untuk mengembangkan aplikasi berbasis website maupun desktop.
Kerangka kerja disini sangat membantu developer dalam menuliskan sebuah dengan lebih 
terstruktur dan tersusun rapi. Kerangka kerja diciptakan untuk mempermudah kinerja dari programmer. 
Sehingga, seorang programmer tidak perlu untuk menuliskan kode secara berulang – ulang.
Karena di dalamnya sendiri anda hanya perlu menyusun komponen – komponen pemrograman saja.</p></br>

<p>> Fungsi Framework :</p>
<p>1. Membuat kode program lebih terstruktur</p>
<p>2. Membantu kinerja dari developer</p>
<p>3. Meningkatkan keamanan software atau website</p>
<p>4. Pemeliharaan dan dokumentasi lebih mudah</p>
<p>5. Mempercepat dalam pembuatan website</p></br>

<p>> Jenis-Jenis Framework :</p>
<p>1. Framework CSS</p>
<p>2. Framework Javascript</p>
<p>3. Framework PHP</p>
